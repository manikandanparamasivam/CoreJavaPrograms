public class ControlProgram10
{
    public static void main(String[] args)
    {
        int a=1;
        for(int i=1;i<=10;i++)
        {
  
            while(a<=10){
                // System.out.println(i+"*"+a+"="+(a*i));
                System.out.println(i+"x"+a+"="+(a*i));
                 a++;
                 
            }
            a=1;
        }


    }
}